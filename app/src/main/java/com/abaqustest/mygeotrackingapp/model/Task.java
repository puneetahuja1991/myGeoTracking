package com.abaqustest.mygeotrackingapp.model;

/**
 * The type Task.
 *
 * @author Puneet Ahuja
 */
public class Task {
    private String id;
    private String task;
    private int state;

    /**
     * Gets id.
     *
     * @return the id
     */
    public String getId() {
        return id;
    }


    /**
     * Gets task.
     *
     * @return the task
     */
    public String getTask() {
        return task;
    }

    /**
     * Gets state.
     *
     * @return the state
     */
    public int getState() {
        return state;
    }

}
